from pyrogram.types import (
    InlineKeyboardButton,
    InlineKeyboardMarkup,
    InlineQueryResultPhoto,
)
from youtubesearchpython.__future__ import VideosSearch

from config import BANNED_USERS
from YukkiMusic import app
from YukkiMusic.utils.inlinequery import answer


@app.on_inline_query(~BANNED_USERS)
async def inline_query_handler(client, query):
    text = query.query.strip().lower()
    answers = []
    if text.strip() == "":
        try:
            await client.answer_inline_query(query.id, results=answer, cache_time=10)
        except Exception:
            return
    else:
        a = VideosSearch(text, limit=20)
        result = (await a.next()).get("result")
        for x in range(15):
            title = (result[x]["title"]).title()
            duration = result[x]["duration"]
            views = result[x]["viewCount"]["short"]
            thumbnail = result[x]["thumbnails"][0]["url"].split("?")[0]
            channellink = result[x]["channel"]["link"]
            channel = result[x]["channel"]["name"]
            link = result[x]["link"]
            published = result[x]["publishedTime"]
            description = f"{views} | {duration} Mins | {channel}  | {published}"
            buttons = InlineKeyboardMarkup(
                [
                    [
                        InlineKeyboardButton(
                            text="üé• ·¥°·¥Ä·¥õ·¥Ñ ú ·¥è…¥  è·¥è·¥ú·¥õ·¥ú ô·¥á",
                            url=link,
                        )
                    ],
                ]
            )
            searched_text = f"""
<blockquote>‚ùáÔ∏è<b>Title:</b> <a href="{link}">{title}</a></blockquote>
<blockquote>‚è≥<b>Duration:</b> {duration} Mins</blockquote>
<blockquote>üëÄ<b>Views:</b> <code>{views}</code></blockquote>
<blockquote>‚è∞<b>Published:</b> {published}</blockquote>
<blockquote>üé•<b>Channel:</b> {channel}</blockquote>
<blockquote>üìé<b>Source Link:</b> <a href="{channellink}">Visit</a></blockquote>

<i>Reply with /ply on this searched message to stream it on Voice Chat.</i>

<blockquote>‚ö°Ô∏è Powered by {app.mention}<blockquote>"""
            answers.append(
                InlineQueryResultPhoto(
                    photo_url=thumbnail,
                    title=title,
                    thumb_url=thumbnail,
                    description=description,
                    caption=searched_text,
                    reply_markup=buttons,
                )
            )
        try:
            return await client.answer_inline_query(query.id, results=answers)
        except Exception:
            return
